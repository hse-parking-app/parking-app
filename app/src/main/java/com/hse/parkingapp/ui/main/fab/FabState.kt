package com.hse.parkingapp.ui.main.fab

/**
 * Enum class representing the state of a Floating Action Button (FAB).
 * It can be either COLLAPSED or EXPANDED.
 */
enum class FabState {
    /**
     * The FAB is in a collapsed state.
     */
    COLLAPSED,

    /**
     * The FAB is in an expanded state
     */
    EXPANDED
}
